package com.ipablive.vo;

/**
 * @author 
 *
 */
public class CityVO 
{
	private int id;
	private int state;
	private String cityName;
	
	public int getId() 
	{
		return id;
	}
	public void setId(int id) 
	{
		this.id = id;
	}
	
	public int getState() 
	{
		return state;
	}
	public void setState(int state) 
	{
		this.state = state;
	}
	
	public String getCityName() 
	{
		return cityName;
	}
	public void setCityName(String cityName) 
	{
		this.cityName = cityName;
	}
}
