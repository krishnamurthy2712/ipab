public class BribeAnalysisVO 
{
	private int bribeReportsCount;
	private int totalCity;
	private int totalAmount;
	
	public int getBribeReportsCount() 
	{
		return bribeReportsCount;
	}
	public void setBribeReportsCount(int bribeReportsCount) 
	{
		this.bribeReportsCount = bribeReportsCount;
	}
	
	public int getTotalCity() 
	{
		return totalCity;
	}
	public void setTotalCity(int totalCity) 
	{
		this.totalCity = totalCity;
	}
	
	public int getTotalAmount() 
	{
		return totalAmount;
	}
	public void setTotalAmount(int totalAmount) 
	{
		this.totalAmount = totalAmount;
	}
	
	
}
